    //--------------------------------------------------------------------------------------------------------
    // Search
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $str
    // @param string $needle
    // @param string $type, Options: string, position
    // @param string $case
    //
    //--------------------------------------------------------------------------------------------------------
    public function use(String $str, String $needle, String $type = 'string', Bool $case = true);

    //--------------------------------------------------------------------------------------------------------
    // Position
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $str
    // @param string $needle
    // @param string $case
    //
    //--------------------------------------------------------------------------------------------------------
    public function position(String $str, String $needle, Bool $case = true);

    //--------------------------------------------------------------------------------------------------------
    // String
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $str
    // @param string $needle
    // @param string $case
    //
    //--------------------------------------------------------------------------------------------------------
    public function string(String $str, String $needle, Bool $case = true) : String;
}
