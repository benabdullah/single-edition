<?php 

use ZN\IndividualStructures\Lang;

trait InformationAbility
{
    //--------------------------------------------------------------------------------------------------------
    // Error
    //--------------------------------------------------------------------------------------------------------
    //
    // @var string
    //
    //--------------------------------------------------------------------------------------------------------
    protected $error;

    //--------------------------------------------------------------------------------------------------------
    // Success
    //--------------------------------------------------------------------------------------------------------
    //
    // @var  string
    //
    //--------------------------------------------------------------------------------------------------------
    protected $success;

    //--------------------------------------------------------------------------------------------------------
    // error()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param void
    //
    //--------------------------------------------------------------------------------------------------------
    public function error()
    {
        if( ! empty($this->error) )
        {
            if( is_array($this->error) )
            {
                return implode('<br>', $this->error);
            }

            return $this->error;
        }
        else
        {
            return false;
        }
    }

    //--------------------------------------------------------------------------------------------------------
    // success()
    //--------------------------------------------------------------------------------------------------------
    //
    // @param void
    //
    //--------------------------------------------------------------------------------------------------------
    public function success()
    {
        if( empty($this->error) )
        {
            if( ! empty($this->success) )
            {
                if( is_array($this->success) )
                {
                    return implode('<br>', $this->success);
                }

                return $this->success;
            }
            else
            {
                return Lang::select('Success', 'success');
            }
        }
        else
        {
            return false;
        }
    }

    //--------------------------------------------------------------------------------------------------------
    // Status
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  void
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
    public function status()
    {
        if( $success = $this->success() )
        {
            return $success;
        }

        return $this->error();
    }
}
