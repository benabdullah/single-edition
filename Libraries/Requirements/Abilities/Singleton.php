    //
    //--------------------------------------------------------------------------------------------------------

    //--------------------------------------------------------------------------------------------------------
    // Singleton
    //--------------------------------------------------------------------------------------------------------
    //
    // @var mixed
    //
    //--------------------------------------------------------------------------------------------------------
    protected static $singleton = NULL;
    
    //--------------------------------------------------------------------------------------------------------
    // Singleton
    //--------------------------------------------------------------------------------------------------------
    //
    // @param void
    //
    //--------------------------------------------------------------------------------------------------------
    protected static function singleton()
    {
        if( ! self::$singleton instanceof self ) 
        {
            self::$singleton = new self;
        }

        return self::$singleton;
    }
}
