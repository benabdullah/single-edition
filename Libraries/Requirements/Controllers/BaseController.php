    //--------------------------------------------------------------------------------------------------------
    // Get
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $class
    // @return class
    //
    //--------------------------------------------------------------------------------------------------------
    public function __get($class)
    {
        if( ! isset($this->$class) )
        {
            return $this->$class = uselib($class);
        }
    }

    //--------------------------------------------------------------------------------------------------------
    // Reload
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  void
    // @return this
    //
    //--------------------------------------------------------------------------------------------------------
    public function reload()
    {
        Autoloader::restart();

        return $this;
    }
}

class_alias('ZN\Requirements\Controllers\BaseController', 'BaseController');
