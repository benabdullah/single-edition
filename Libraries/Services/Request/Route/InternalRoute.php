    //--------------------------------------------------------------------------------------------------------
    // URI
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $path
    // @param bool   $usable
    //
    //--------------------------------------------------------------------------------------------------------
    public function uri(String $path = NULL, $usable = true)
    {
        $path = rtrim($path, '/');

        if( ! strstr($path, '/') )
        {
            $path = suffix($path) . Config::get('Services', 'route')['openFunction'];
        }

        $lowerPath = strtolower($path);

        $filters = $this->filters;
        $filters = Arrays::addLast($filters, 'redirect');
        $filters = Arrays::deleteElement($filters, 'usable');

        $this->_filter($filters, $lowerPath);

        if( empty($this->route) )
        {
            return false;
        }

        $configPatternType = Config::get('Services', 'route')['patternType'];

        if( $configPatternType === 'classic' )
        {
            $routeString = Regex::special2classic($this->route);
        }
        elseif( $configPatternType === 'special' )
        {
            $routeString = $this->route;
        }

        $this->routes['changeUri'][$routeString] = $this->_stringRoute($path, $this->route)[$this->route];

        if( $usable === false )
        {
            $this->usable[$lowerPath]['usable'] = $path;
        }

        $this->route = NULL;
    }

