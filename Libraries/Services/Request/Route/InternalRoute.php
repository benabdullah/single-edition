        $this->restore  = NULL;
        $this->csrf     = NULL;
        $this->ajax     = NULL;
        $this->curl     = NULL;
        $this->restful  = NULL;
    }

    //--------------------------------------------------------------------------------------------------------
    // Protected Default Variable -> 4.3.1
    //--------------------------------------------------------------------------------------------------------
    //
    //  @return void
