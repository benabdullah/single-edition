    // @param  string $function
    // @param  int    $time     = 60
    // @param  string $compress = 'gz'
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
    public function view(String $file, Int $time = 60, String $compress = 'gz') : String;

    //--------------------------------------------------------------------------------------------------------
    // File -> 5.3.21
    //--------------------------------------------------------------------------------------------------------
    //
    // @param  string $function
    // @param  int    $time     = 60
    // @param  string $compress = 'gz'
    // @return string
    //
    //--------------------------------------------------------------------------------------------------------
