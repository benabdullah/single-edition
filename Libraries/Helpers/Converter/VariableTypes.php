    //--------------------------------------------------------------------------------------------------------
    // To Int
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toInt($var) : Int
    {
        return (int) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Integer
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toInteger($var) : Int
    {
        return (int) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Bool
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toBool($var) : Bool
    {
        return (bool) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Boolean
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toBoolean($var) : Bool
    {
        return (bool) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To String
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toString($var) : String
    {
        if( is_array($var) || is_object($var) )
        {
            return implode(' ', (array) $var);
        }

        return (string) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Float
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toFloat($var) : Float
    {
        return (float) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Real
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toReal($var)
    {
        return (real) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Double
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toDouble($var)
    {
        return (double) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Object
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toObject($var) : stdClass
    {
        return (object) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Object Recursive -> 5.0.0
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toObjectRecursive($var) : stdClass
    {
        $object = new stdClass;

        return $this->objectRecursive((array) $var, $object);
    }

    //--------------------------------------------------------------------------------------------------------
    // To Array
    //--------------------------------------------------------------------------------------------------------
    //
    // @param var $var
    //
    //--------------------------------------------------------------------------------------------------------
    public function toArray($var) : Array
    {
        return (array) $var;
    }

    //--------------------------------------------------------------------------------------------------------
    // To Constant
    //--------------------------------------------------------------------------------------------------------
    //
    // @param string $var
    // @param string $prefix
    // @param string $suffix
    //
    //--------------------------------------------------------------------------------------------------------
    public function toConstant(String $var, String $prefix = NULL, String $suffix = NULL)
    {
        $variable = \Autoloader::upper($prefix.$var.$suffix);

        if( defined($variable) )
        {
            return constant($variable);
        }
        elseif( defined($var) )
        {
            return constant($var);
        }
        else
        {
            if( is_numeric($var) )
            {
                return (int) $var;
            }

            return $var;
        }
    }

    //--------------------------------------------------------------------------------------------------
    // Protected Object Recursive
    //--------------------------------------------------------------------------------------------------
    //
    // @param array    $array
    // @param stdClass $obj
    //
    // @return string
    //
    //--------------------------------------------------------------------------------------------------
    protected function objectRecursive(Array $array, stdClass &$std) : stdClass
    {
        foreach( $array as $key => $value )
        {
            if( is_array($value) )
            {
                $std->$key = new stdClass;

                $this->objectRecursive($value, $std->$key);
            }
            else
            {
                $std->$key = $value;
            }
        }

        return $std;
    }
}
